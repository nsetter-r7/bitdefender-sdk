#define SCAN_ERROR_FILE_COPY	  			MAKECODE_SCAN_ERROR(5)
#define SCAN_ERROR_FILE_RENAME		  		MAKECODE_SCAN_ERROR(6)
#define SCAN_ERROR_FILE_MOVE		  		MAKECODE_SCAN_ERROR(7)
#define SCAN_ERROR_DIR_FIND		 			MAKECODE_SCAN_ERROR(0xB)
#define SCAN_ERROR_FN_INIT		 			MAKECODE_SCAN_ERROR(0xF)
#define SCAN_ERROR_NO_MEMORY	 			MAKECODE_SCAN_ERROR(0x10)
#define SCAN_ERROR_PATH_ERROR 				MAKECODE_SCAN_ERROR(0x11)
#define SCAN_ERROR_INVALID_HANDLE			MAKECODE_SCAN_ERROR(0x12)
#define SCAN_ERROR_DISINFECT	 			MAKECODE_SCAN_ERROR(0x13)
#define SCAN_ERROR_NOT_SUPPORTED 			MAKECODE_SCAN_ERROR(0x15)
#define SCAN_ERROR_PARAM2		 			MAKECODE_SCAN_ERROR(0x17)
#define SCAN_ERROR_PARAM3		 			MAKECODE_SCAN_ERROR(0x18)
#define SCAN_ERROR_PARAM4		 			MAKECODE_SCAN_ERROR(0x19)
