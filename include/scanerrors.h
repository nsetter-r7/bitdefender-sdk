#define SCAN_ERROR_INVALID_PARAMETER   		MAKECODE_SCAN_ERROR(0x21)
#define SCAN_ERROR_PARAM1					MAKECODE_SCAN_ERROR(0x22)
#define SCAN_ERROR_CACHE_SYNTAX 			MAKECODE_SCAN_ERROR(0x23)
#define SCAN_ERROR_DATABASE_INTEGRITY  		MAKECODE_SCAN_ERROR(0x24)

// CORE informations
#define SCAN_INFORM_STOPPED  				MAKECODE_SCAN_INFO(1)
#define SCAN_INFORM_NO_PLUGINS  			MAKECODE_SCAN_INFO(3)
#define SCAN_INFORM_MAXFILES            	MAKECODE_SCAN_INFO(4)
#define SCAN_INFORM_MAXLEVELS           	MAKECODE_SCAN_INFO(5)
